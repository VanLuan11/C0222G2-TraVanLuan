package ss10_dsa_list.bai_tap.LinkedList;

public class MyLinkedList {
    private int id;
    private String name;

    public MyLinkedList() {

    }

    public MyLinkedList(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }

}
